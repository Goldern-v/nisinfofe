<template>
  <div>
    <slot :images="images" :options="options">
    </slot>
  </div>
</template>
<script>
import Viewer from 'viewerjs'
import 'viewerjs/dist/viewer.css'
const defaults = {
  zIndex: 100000000
}
export default {
  props: {
    images: {
      type: Array,
      required: true
    },
    options: {
      type: Object
    }
  },

  data () {
    return {
    }
  },

  computed: {
  },

  methods: {
    createViewer () {
      const options = Object.assign({}, defaults, this.options)
      this.$viewer && this.$viewer.destroy()
      this.$viewer = new Viewer(this.$el, options)
      this.$emit('inited', this.$viewer)
    }
  },

  watch: {
    images () {
      this.$nextTick(() => {
        setTimeout(()=>{
          this.createViewer()
        },3000)
      })
    },
    options: {
      handler: function () {
        this.$nextTick(() => {
          this.createViewer()
        })
      },
      deep: true
    }
  },

  mounted () {
    this.createViewer()
  },

  destroyed () {
    this.$viewer && this.$viewer.destroy()
  }
}
</script>
