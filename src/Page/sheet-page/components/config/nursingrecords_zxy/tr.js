export function getListData() {
  let list = [
    "南方中西医:护理记录单:意识",
  ];

  multiDictInfo(list).then(res => {
    let data = res.data.data;
    setList(ysList, list[0], data);
  });
}

getListData();
/**
 *
 * @param {*} list 原数组
 * @param {*} key 对应的key
 * @param {*} data 数据源
 */
function setList(list, key, data) {
  list.splice(0, list.length);
  for (let item of data[key]) {
    list.push(item.name);
  }
}

